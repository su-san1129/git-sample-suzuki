
public class Car {
	
	int speed;
	int name;
	int gas;

	private int tire;


	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	public int getGas() {
		return gas;
	}
	public void setGas(int gas) {
		this.gas = gas;
	}
	
	/**
	 * @return the tire
	 */
	public int getTire() {
		return tire;
	}

	/**
	 * @param tire the tire to set
	 */
	public void setTire(int tire) {
		this.tire = tire;
	}
	

}
